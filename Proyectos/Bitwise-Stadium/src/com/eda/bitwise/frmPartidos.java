    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String eqLocal = tbEquipoLocal.getText();
        String eqVisitante = tbEquipoVisitante.getText();
        String fecha = tbFecha.getText();
        String hora = tbHora.getText();
        Partido partido = new Partido(eqLocal, eqVisitante, fecha, hora);
        partido.registrar();
        estadio.cargarPartidos();
        mostrar();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int id = Integer.parseInt(tbIdPartido.getText());
        String eqLocal = tbEquipoLocal.getText();
        String eqVisitante = tbEquipoVisitante.getText();
        String fecha = tbFecha.getText();
        String hora = tbHora.getText();
        Partido partido = new Partido(id, eqLocal, eqVisitante, fecha, hora);
        partido.editar();
        estadio.cargarPartidos();
        mostrar();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int id = Integer.parseInt(tbIdPartido.getText());
        Partido partidoAux = new Partido();
        partidoAux.setIdPartido(id);
        partidoAux.eliminar();
        estadio.cargarPartidos();
        mostrar();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnIniciarPartidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarPartidoActionPerformed
        if (estadio.getPartidos().size() != 0) {
            Partido nuevoActual;
            if (estadio.getPartidoActual() == null) {
                nuevoActual = estadio.getPartidos().get(0);
                estadio.setPartidoActual(nuevoActual);
            } else {
                Partido actual = estadio.getPartidoActual();
                int index = estadio.getPartidos().indexOf(actual);
                System.out.println(actual.equipo_local);
                if (estadio.getPartidos().size() - 1 >= index + 1) {
                    nuevoActual = estadio.getPartidos().get(index + 1);
                    estadio.setPartidoActual(nuevoActual);
                } else {
                    JOptionPane.showMessageDialog(null, "Debe programar otro partido");
                }

            }
            String jugando = estadio.getPartidoActual().getEquipoLocal().toUpperCase() + " vs " + estadio.getPartidoActual().getEquipoVisitante().toUpperCase();
            lbJugando.setText("PARTIDO ACTUAL: " + jugando);
        } else {
            JOptionPane.showMessageDialog(null, "Debe programar al menos un partido");
        }

    }//GEN-LAST:event_btnIniciarPartidoActionPerformed


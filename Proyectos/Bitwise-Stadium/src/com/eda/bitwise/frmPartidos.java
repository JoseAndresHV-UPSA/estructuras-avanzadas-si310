    public void mostrar() {
        String[] nombreColumnas = new String[]{"Id Partido", "Equipo Local", "Equipo Visitante", "Fecha", "Hora"};
        Object[][] data = new Object[10][5];
        int i = 0;
        while (i < estadio.getPartidos().size()) {
            Partido partidoAux = estadio.getPartidos().get(i);
            data[i][0] = partidoAux.getIdPartido();
            data[i][1] = partidoAux.getEquipoLocal();
            data[i][2] = partidoAux.getEquipoVisitante();
            data[i][3] = partidoAux.getFecha();
            data[i][4] = partidoAux.getHora();
            i++;
        }
        DefaultTableModel tabla = new DefaultTableModel(data, nombreColumnas);
        jtbPartidos.setModel(tabla);
    }

